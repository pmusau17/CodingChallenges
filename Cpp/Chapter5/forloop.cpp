// for loop.cpp -- introducing the for loop
#include <iostream>

int main()
{
    using namespace std;
    int i;  // create a counter
    // initialize; test ; update
    for (i=0; i<5;i++){
        cout << "C++ knows loops." << endl;
    }
    cout << "C++ knows when to stop." << endl;
    return 0;
}

